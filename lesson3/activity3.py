def add(x,y):
    return x+y
def subtract(x,y):
    return x-y
def multiply(x,y):
    return x*y
def divide(x,y):
    return x/y
x=int(input("enter your first number "))
y=int(input("enter your second number "))
print("addition: ",add(x,y))
print("subtraction: ",subtract(x,y))
print("multiplication: ",multiply(x,y))
print("division: ",divide(x,y))